public class BasicEvent {

    /*
        低级事件
        ComponentEvent 组件事件，组件尺寸发生变化，位置移动，显示隐藏等等触发该事件
        ContainerEvent 容器，当容器发生添加组件，删除组件时触发
        WindowEvent 窗口事件，窗口发生改变时，打开，关闭，最大最小化等等
        FocusEvent 焦点事件
        KeyEvent 键盘事件，按下，松开等等
        MouseEvent 鼠标事件，单击，双击松开移动等等
        PaintEvent 绘画事件

        高级事件
        ActionEvent 动作事件，按钮被单机，Text 按下 Enter 键等
        AdjustmentEvent 调节事件，滑动条调节
        ItemEvent 选项事件，选中某项时
        TextEvent 文本事件，文本框文本改变时触发
     */
}
