package src.day40Apr13th;

/**
 * This class use for pair key and value
 */
public class Word {
    String key;
    int value;

    /**
     * This is a constructor.
     */
    public Word() {

    }

    /**
     * get key methods.
     * @return key.
     */
    public String getKey() {
        return key;
    }

    /**
     * Set the key.
     * @param key parameter.
     */
    public void setKey(String key) {
        this.key = key;
    }

    /**
     * Get the value.
     * @return value.
     */
    public int getValue() {
        return value;
    }

    /**
     * Set value.
     * @param value the parameter.
     */
    public void setValue(int value) {
        this.value = value;
    }
}
